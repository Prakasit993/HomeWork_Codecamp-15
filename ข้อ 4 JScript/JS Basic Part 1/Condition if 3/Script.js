let score = Number(prompt("What is your score"));
if (score >= 80) {
  alert("Your grade is A");
  console.log("A");
} else if (score >= 70) {
  alert("Your grade is B");
  console.log("B");
} else if (score >= 60) {
  alert("Your grade is C");
  console.log("C");
} else if (score >= 50) {
  alert("Your grade is D");
  console.log("D");
} else {
  alert("Your grade is F");
  console.log("F");
}
